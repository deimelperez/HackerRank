def formingMagicSquare(s):
    c = 0
    costs = []
    pre = [
        [[8, 1, 6], [3, 5, 7], [4, 9, 2]],
        [[6, 1, 8], [7, 5, 3], [2, 9, 4]],
        [[4, 9, 2], [3, 5, 7], [8, 1, 6]],
        [[2, 9, 4], [7, 5, 3], [6, 1, 8]],
        [[8, 3, 4], [1, 5, 9], [6, 7, 2]],
        [[4, 3, 8], [9, 5, 1], [2, 7, 6]],
        [[6, 7, 2], [1, 5, 9], [8, 3, 4]],
        [[2, 7, 6], [9, 5, 1], [4, 3, 8]],
    ]
    for ms in pre:
        c = 0
        for row_s, row_pre in zip(s, ms):
            for i, j in zip(row_s, row_pre):
                if i != j:
                    c += abs(i - j)
        costs.append(c)

    return min(costs)


